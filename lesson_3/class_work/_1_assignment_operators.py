"""
    Операторы присваивания: +=, -=, *=, /=, **=, %=, //=

    Используются для того, чтобы применить операцию
    и присвоить результат той же переменной.
"""

a = 10

# увеличить а на 1
a += 1  # a = a + 1

# уменьшить a в 20 раз
a /= 20  # a = a / 20

# возвести в квадрат
a **= 2  # a = a ** 2
